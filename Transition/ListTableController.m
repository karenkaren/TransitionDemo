#import "ListTableController.h"

@interface ListTableController ()

@property (nonatomic, strong) NSArray * data;

@end

@implementation ListTableController

- (void)viewDidLoad {
    [super viewDidLoad];
    [self initDatas];
}

- (void)initDatas
{
    _data = @[@"翻页效果", @"扩散效果", @"QQ音乐"];
}

#pragma mark - Table view data source
- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView {
    return 1;
}

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section {
    return _data.count;
}

- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath {
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:@"kBaseCell" forIndexPath:indexPath];
    
    cell.textLabel.text = _data[indexPath.row];
    
    return cell;
}

- (void)tableView:(UITableView *)tableView didSelectRowAtIndexPath:(NSIndexPath *)indexPath
{
    if (indexPath.row == 0) {
        [self performSegueWithIdentifier:@"kPushPageCover" sender:nil];
    } else if (indexPath.row == 1){
        [self performSegueWithIdentifier:@"kPushSpread" sender:nil];
    } else {
        [self performSegueWithIdentifier:@"kPushQQMusic" sender:nil];
    }
}

